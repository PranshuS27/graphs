package spiderman;
import java.util.*;

public class weightedNode {
    int value; 
    int weight; 
    
    public weightedNode(int value, int weight)
    {
        this.value = value; 
        this.weight = weight; 
    }
    
    public int getValue()
    {
        return value; 
    }
    public int getWeight()
    {
        return weight; 
    }
}
